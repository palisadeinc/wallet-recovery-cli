// TestRecoverED25519PrivateKey tests the ED25519 recovery functionality
func TestRecoverED25519PrivateKey(t *testing.T) {
	// Generate test UUIDs
	quorumID := uuid.New()
	keyID := uuid.New()
	ersLabel := sha256.Sum256(fmt.Appendf(nil, "%s-%s", quorumID, keyID))

	// Generate test ED25519 key
	masterPrivateKey := make([]byte, 32)
	if _, err := rand.Read(masterPrivateKey); err != nil {
		t.Fatalf("Failed to generate master private key: %v", err)
	}

	masterChainCode := make([]byte, 32)
	if _, err := rand.Read(masterChainCode); err != nil {
		t.Fatalf("Failed to generate master chain code: %v", err)
	}

	// Generate recovery data (this would normally come from TSM SDK)
	// Note: This is a mock test - in real usage, recovery data would be generated by the TSM SDK
	t.Logf("Generated test keys for ED25519 recovery")
	t.Logf("Master private key (hex): %s", hex.EncodeToString(masterPrivateKey))
	t.Logf("ERS Label: %x", ersLabel)

	// Test GetSolanaAddressFromPrivateKeyBytes
	testPrivateKey := make([]byte, 32)
	if _, err := rand.Read(testPrivateKey); err != nil {
		t.Fatalf("Failed to generate test private key: %v", err)
	}

	solanaAddress, err := GetSolanaAddressFromPrivateKeyBytes(testPrivateKey)
	if err != nil {
		t.Fatalf("Failed to get Solana address: %v", err)
	}

	t.Logf("Generated Solana address: %s", solanaAddress)

	// Test with invalid key size
	invalidKey := make([]byte, 64) // Wrong size
	_, err = GetSolanaAddressFromPrivateKeyBytes(invalidKey)
	if err == nil {
		t.Error("Expected error for invalid key size, got nil")
	}
}

